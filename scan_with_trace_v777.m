function [execution_time] = scan_with_trace_v777(r_start, r_end)
    % =========================================================================
    % Оптимизированный алгоритм сканирования с использованием нормы и трейса
    % Данная функция выполняет адаптивное сканирование интервала [r_start, r_end]
    % =========================================================================

    % --- Параметры алгоритма ---
    dr_min = 1e-4;   % Минимальный шаг сканирования
    dr_max = 0.05;   % Максимальный шаг сканирования
    r = r_start;     % Начальная точка
    
    % --- Инициализация хранилищ данных ---
    res_r = []; 
    res_phi = []; 
    res_norm = [];

    % --- Начальные вычисления ---
    [norm_curr, k_curr] = get_H_data(r);
    phi_curr = 1 / norm_curr;
    step = dr_max;
    
    % Запись начальных значений
    res_norm(1) = norm_curr;
    res_r(1)    = r;
    res_phi(1)  = phi_curr;
    
    t_start = clock; % Фиксация времени начала работы

    % --- Основной цикл сканирования ---
    while r <= r_end
        % Попытка сделать следующий шаг
        r_next = r + step;
        
%         % Ограничение r_next значением r_end
%         if r_next > r_end
%             r_next = r_end;
%             step = r_next - r;
%         end
        
        [norm_next, k_next] = get_H_data(r_next);
        phi_next = 1 / norm_next;

        % --- Контроль изменения количества собственных значений (Trace) ---
        % Если обнаружено изменение индекса k, уменьшаем шаг для уточнения границы
        if (k_next ~= k_curr) && (step > dr_min)
            step = step / 2;
            continue; % Повторная итерация с уменьшенным шагом
        end

        % --- Защита от резких изменений нормы (резкий спад phi) ---
        if (phi_next < phi_curr * 0.1) && (step > dr_min)
            step = step / 2;
            continue;
        end

        % --- Сохранение результатов при успешном шаге ---
        res_r(end+1)    = r_next;
        res_phi(end+1)  = phi_next;
        res_norm(end+1) = norm_next;
     
        % --- Адаптивный расчет следующего шага на основе градиента ---
        slope = abs(phi_next - phi_curr) / step;
        
        if slope < 1e-10
            step = dr_max;
        else
            % Масштабирование шага: шаг обратно пропорционален крутизне склона
            step = max(dr_min, min(dr_max, phi_next / slope));
        end

        % --- Обновление текущих состояний ---
        r = r_next;
        phi_curr = phi_next;
        k_curr   = k_next;

    end
    
    % Возврат времени выполнения
    execution_time = etime(clock, t_start);
    
    % --- Визуализация (закомментирована) ---
%     figure; plot(res_r, log10(res_phi), 'b'); grid on; title('Log10 Phi');
    
    figure; plot(res_r, log10(res_norm), 'r');
    xlabel('r');
    ylabel('log10(||H||)');
end

function [norm_H, k] = get_H_data(r_val)
    % =========================================================================
    % Вспомогательная функция для вычисления нормы матрицы H и индекса k (Trace)
    % =========================================================================
    
    w_max = 10^10; % Максимальный порог веса
    ip = 10^-10;    % Точность сходимости
    
    try
        A = mat(r_val); 
        [n, ~] = size(A); 
        I = eye(n);
        
        % Инициализация переменных для итерационного процесса
        A0 = A'; B0 = I; p = 2*I; d = 0;
        
        % Расчет максимального количества итераций
        m0 = round(log2(- (1 + w_max) * log(ip / ((2 + 2 * ip) * sqrt(w_max)))));
        
        % Основной итерационный цикл (метод QR-декомпозиции)
        while (max(abs(p * p - p), [], 'all') > ip && d <= m0 / 3)
            d = d + 1;
            for i = 1:3
                [Q, ~] = qr([-B0; A0]); 
                QQ = Q';
                % Обновление блоков матриц
                A0 = QQ(n+1:2*n, 1:n) * A0; 
                B0 = QQ(n+1:2*n, n+1:2*n) * B0;
            end
            % Вычисление проектора
            p = -((A0 - B0) \ B0); 
        end
        
        % Вычисление количества собственных значений через след (Trace)
        % Используется округление и вещественная часть для нивелирования шумов
        k = round(real(trace(p)));
        
        % Вычисление нормы матрицы H
        inv_su = (B0 + A0) \ I;
        H      = inv_su * inv_su';
        norm_H = max(abs(H), [], 'all');
        
    catch
        % Обработка исключений при сингулярности или ошибках вычислений
        norm_H = NaN; 
        k = 0;
    end
    
    % Проверка на корректность результата
    if isnan(norm_H) || norm_H > w_max
        norm_H = NaN; 
    end
end