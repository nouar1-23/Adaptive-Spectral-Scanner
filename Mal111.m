function [execution_time] = Mal111(r_start, r_end)
    % =========================================================================
    % Упрощенная функция сканирования с фиксированным шагом
    % Выполняет последовательный расчет нормы без адаптации шага
    % =========================================================================

    h = 0.01;        % Фиксированный шаг (Шаг)
    r = r_start;     % Начальная точка
    
    % --- Инициализация хранилищ данных ---
    res_r = []; 
    res_norm = [];

    t_start = clock; % Фиксация времени начала работы

    % --- Основной цикл сканирования ---
    while r <= r_end
        
        % Вычисление данных для текущего значения r
        [norm_next] = get_H_data(r);

        % --- Сохранение результатов ---
        res_r(end+1)    = r;
        res_norm(end+1) = norm_next;
        
        % --- Инкремент значения r ---
        r = r + h;
    end
    
    % Возврат времени выполнения
    execution_time = etime(clock, t_start);
    
    % --- Визуализация данных ---
    hold on;
    plot(res_r, log10(res_norm), 'b'); 
%     xlabel('r');
%     ylabel('log10(||H||)');
%     title('Результаты сканирования (Фиксированный шаг)');
end

function [norm_H] = get_H_data(r_val)
    % =========================================================================
    % Вспомогательная функция для вычисления нормы матрицы H
    % Использует итерационный метод на основе QR-разложения
    % =========================================================================
    
    w_max = 10^10; % Максимальный допустимый порог
    ip = 10^-10;    % Точность сходимости
    
    try
        A = mat(r_val); 
        [n, ~] = size(A); 
        I = eye(n);
        
        % Инициализация переменных
        A0 = A'; B0 = I; p = 2*I; d = 0;
        
        % Расчет лимита итераций m0
        m0 = round(log2(- (1 + w_max) * log(ip / ((2 + 2 * ip) * sqrt(w_max)))));
        
        % Цикл итерационного уточнения (Метод структурной декомпозиции)
        while (max(abs(p * p - p), [], 'all') > ip && d <= m0 / 3)
            d = d + 1;
            for i = 1:3
                [Q, ~] = qr([-B0; A0]); 
                QQ = Q';
                A0 = QQ(n+1:2*n, 1:n) * A0; 
                B0 = QQ(n+1:2*n, n+1:2*n) * B0;
            end
            % Обновление проектора p
            p = -((A0 - B0) \ B0); 
        end
   
        % Вычисление финальной матрицы H и ее нормы
        inv_su = (B0 + A0) \ I;
        H      = inv_su * inv_su';
        norm_H = max(abs(H), [], 'all');
        
    catch
        % Обработка ошибок при работе с вырожденными матрицами
        norm_H = NaN; 
    end
    
    % Валидация результата
    if isnan(norm_H) || norm_H > w_max
        norm_H = NaN; 
    end
end